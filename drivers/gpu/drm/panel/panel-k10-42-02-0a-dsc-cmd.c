static void get_panel_id(char *panel_id)
{
	int ret = -1;
	char panel_id_tb[] = {0xA2};
	struct mtk_ddic_dsi_msg *cmd_msg = NULL;

	cmd_msg = vmalloc(sizeof(struct mtk_ddic_dsi_msg));
	if (unlikely(!cmd_msg)) {
		pr_err("%s vmalloc mtk_ddic_dsi_msg is failed, return\n", __func__);
		return;
	}
	memset(cmd_msg, 0, sizeof(struct mtk_ddic_dsi_msg));

	cmd_msg->channel = 0;
	cmd_msg->flags = 2;
	cmd_msg->tx_cmd_num = 1;

	cmd_msg->type[0] = 0x06;
	cmd_msg->tx_buf[0] = panel_id_tb;
	cmd_msg->tx_len[0] = ARRAY_SIZE(panel_id_tb);

	cmd_msg->rx_cmd_num = 1;
	cmd_msg->rx_buf[0] = panel_id;
	cmd_msg->rx_len[0] = 2;
	ret = mtk_ddic_dsi_read_cmd(cmd_msg);
	if (ret != 0) {
		DDPPR_ERR("%s mtk_ddic_dsi_read_cmd error\n", __func__);
	}
	pr_err("%s panel id = 0x%x, 0x%x\n", __func__, panel_id[0], panel_id[1]);
	return;
}

