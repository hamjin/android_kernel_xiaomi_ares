static void mode_switch_60_to_120(struct drm_panel *panel,
	enum MTK_PANEL_MODE_SWITCH_STAGE stage)
{
	struct lcm *ctx = panel_to_lcm(panel);

	pr_info("%s %d\n", __func__, stage);
	if (stage == BEFORE_DSI_POWERDOWN) {
		/* display off */
		//lcm_dcs_write_seq_static(ctx, 0x28);
		//msleep(10);
		//lcm_dcs_write_seq_static(ctx, 0x10);
		//usleep_range(200 * 1000, 230 * 1000);
	} else if (stage == AFTER_DSI_POWERON) {
		/* Frequency Select 120HZ*/
		lcm_dcs_write_seq_static(ctx, 0x2F, 0x02);
		lcm_dcs_write_seq_static(ctx, 0xF0, 0x55, 0xAA, 0x52, 0x08, 0x01);
		lcm_dcs_write_seq_static(ctx, 0xC3, 0x94, 0x01, 0x8F, 0xD0, 0x20, 0x01, 0x00);
		ctx->dynamic_fps = 120;

		g_notify_data.disp_id = MI_DISPLAY_PRIMARY;
		g_notify_data.data = &ctx->dynamic_fps;
		mi_disp_notifier_call_chain(MI_DISP_CHANGE_FPS, &g_notify_data);
		mi_disp_feature_sysfs_notify(MI_SYSFS_DYNAMIC_FPS);
	}
	/* add for display fps count */
	dsi_panel_fps_count(ctx, 120, 1);
}

static void mode_switch_120_to_60(struct drm_panel *panel,
	enum MTK_PANEL_MODE_SWITCH_STAGE stage)
{
	struct lcm *ctx = panel_to_lcm(panel);

	pr_info("%s %d\n", __func__, stage);
	if (stage == BEFORE_DSI_POWERDOWN) {
		/* display off */
		//lcm_dcs_write_seq_static(ctx, 0x28);
		//msleep(10);
		//lcm_dcs_write_seq_static(ctx, 0x10);
		//usleep_range(200 * 1000, 230 * 1000);
	} else if (stage == AFTER_DSI_POWERON) {
		/* Frequency Select 60HZ*/
		lcm_dcs_write_seq_static(ctx, 0x2F, 0x01);
		mtk_drm_crtc_fps_switch_mode_wait();
		lcm_dcs_write_seq_static(ctx, 0xF0, 0x55, 0xAA, 0x52, 0x08, 0x01);
		lcm_dcs_write_seq_static(ctx, 0xC3, 0x8C, 0x01, 0x98, 0xD0, 0x20, 0x01, 0x00);
		ctx->dynamic_fps = 60;

		g_notify_data.disp_id = MI_DISPLAY_PRIMARY;
		g_notify_data.data = &ctx->dynamic_fps;
		mi_disp_notifier_call_chain(MI_DISP_CHANGE_FPS, &g_notify_data);
		mi_disp_feature_sysfs_notify(MI_SYSFS_DYNAMIC_FPS);
	}
	/* add for display fps count */
	dsi_panel_fps_count(ctx, 60, 1);
}

static int mode_switch(struct drm_panel *panel, unsigned int cur_mode,
		unsigned int dst_mode, enum MTK_PANEL_MODE_SWITCH_STAGE stage)
{
	int ret = 0;
	struct lcm *ctx;
	if (!panel) {
		pr_err("invalid params\n");
		return -EAGAIN;
	}

	ctx = panel_to_lcm(panel);
	if (!get_dsi_doze_state() && !ctx->doze_brightness_state) {
		if (cur_mode == 0 && dst_mode == 1) { /* 60 switch to 120 */
			mode_switch_60_to_120(panel, stage);
		} else if (cur_mode == 1 && dst_mode == 0) { /* 120 switch to 60 */
			mode_switch_120_to_60(panel, stage);
		} else
			ret = 1;
	}
	return ret;
}

