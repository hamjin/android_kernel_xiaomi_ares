static int lcm_dcs_read(struct lcm *ctx, u8 cmd, void *data, size_t len)
{
	struct mipi_dsi_device *dsi = to_mipi_dsi_device(ctx->dev);
	ssize_t ret;

	if (ctx->error < 0)
		return 0;

	ret = mipi_dsi_dcs_read(dsi, cmd, data, len);
	if (ret < 0) {
		dev_err(ctx->dev, "error %d reading dcs seq:(%#x)\n", ret, cmd);
		ctx->error = ret;
	}

	return ret;
}

static void lcm_panel_get_data(struct lcm *ctx)
{
	u8 buffer[3] = {0};
	static int ret;

	if (ret == 0) {
		ret = lcm_dcs_read(ctx, 0x0A, buffer, 1);
		dev_info(ctx->dev, "return %d data(0x%08x) to dsi engine\n",
			 ret, buffer[0] | (buffer[1] << 8));
	}
}
#endif

static void lcm_dcs_write(struct lcm *ctx, const void *data, size_t len)
{
	struct mipi_dsi_device *dsi = to_mipi_dsi_device(ctx->dev);
	ssize_t ret;
	char *addr;

	if (ctx->error < 0)
		return;

	addr = (char *)data;
	if ((int)*addr < 0xB0)
		ret = mipi_dsi_dcs_write_buffer(dsi, data, len);
	else
		ret = mipi_dsi_generic_write(dsi, data, len);
	if (ret < 0) {
		dev_err(ctx->dev, "error %zd writing seq: %ph\n", ret, data);
		ctx->error = ret;
	}
}

static int mi_panel_send_package_dsi_cmd(struct lcm *ctx, struct panel_package_cmd_set cmd)
{
	int rc = 0, i = 0;
	struct panel_package_dsi_cmd_desc *cmds;
	u32 count;

	cmds = cmd.cmds;
	count = cmd.count;
	pr_err("panel_send_cmds: mi_panel_send_package_dsi_cmd cmd.count = %d\n", cmd.count);

	if (count == 0) {
		pr_err("panel_send_cmds: No commands to be sent\n");
		goto error;
	}

	for (i = 0; i < count; i++) {
		lcm_dcs_write(ctx, cmds->msg.tx_buf, cmds->msg.tx_len);

		if (cmds->post_wait_ms)
			usleep_range(cmds->post_wait_ms*1000,
					((cmds->post_wait_ms*1000)+10));
		cmds++;
	}
error:
	return rc;
}

static int mi_panel_send_package_dsi_cmd_panel(struct drm_panel *panel)
{
	int rc = 0;
	struct lcm *ctx = panel_to_lcm(panel);
	if (!ctx->enabled)
		return 0;

	rc = mi_panel_send_package_dsi_cmd(ctx, gcmd);
	return rc;
}

static void push_table(struct lcm *ctx, struct LCM_setting_table *table, unsigned int count)
{
	unsigned int i, j;
	unsigned char temp[80] = {0};
	for (i = 0; i < count; i++) {
		unsigned cmd;
		cmd = table[i].cmd;
		memset(temp, 0, sizeof(temp));
		switch (cmd) {
		case REGFLAG_DELAY:
			if (table[i].count <= 10)
				msleep(table[i].count);
			else
				msleep(table[i].count);
			break;
		case REGFLAG_END_OF_TABLE:
			break;
		default:
			temp[0] = cmd;
			for (j = 0; j < table[i].count; j++) {
				temp[j+1] = table[i].para_list[j];
			}
			lcm_dcs_write(ctx, temp, table[i].count+1);
		}
	}
}

static void lcm_panel_init(struct lcm *ctx)
{
	pr_info("%s+ \n", __func__);
	gpiod_set_value(ctx->reset_gpio, 0);
	usleep_range(10 * 1000, 10 * 1000 + 10);
	gpiod_set_value(ctx->reset_gpio, 1);
	usleep_range(1 * 1000, 1 * 1000 + 10);
	gpiod_set_value(ctx->reset_gpio, 0);
	usleep_range(30, 30 + 10);
	gpiod_set_value(ctx->reset_gpio, 1);
	usleep_range(10 * 1000, 10 * 1000 + 10);
	mi_panel_send_package_dsi_cmd(ctx, gcmd);
	if (ctx->bist_mode_enabled) {
		push_table(ctx, bist_init_setting, sizeof(bist_init_setting) / sizeof(struct LCM_setting_table));
	} else {
		push_table(ctx, init_setting, sizeof(init_setting) / sizeof(struct LCM_setting_table));
	}
	pr_info("%s-\n", __func__);
}

static int lcm_disable(struct drm_panel *panel)
{
	struct lcm *ctx = panel_to_lcm(panel);

	if (!ctx->enabled)
		return 0;

	lcm_dcs_write_seq_static(ctx, 0x28);
	usleep_range(10 * 1000, 10 * 1000 + 10);
	lcm_dcs_write_seq_static(ctx, 0x10);
	usleep_range(120 * 1000, 120 * 1000 + 10);

	if (ctx->backlight) {
		ctx->backlight->props.power = FB_BLANK_POWERDOWN;
		backlight_update_status(ctx->backlight);
	}

	ctx->enabled = false;

	return 0;
}

static int lcm_unprepare(struct drm_panel *panel)
{
	struct lcm *ctx = panel_to_lcm(panel);
	pr_info("%s+\n", __func__);
	if (!ctx->prepared)
		return 0;

	gpiod_set_value(ctx->reset_gpio, 0);
	usleep_range(10, 10 + 10);

	/*Power off VCI_3P0*/
	pmic_set_register_value(PMIC_RG_VIBR_VOSEL, 0xB);
	pmic_set_register_value(PMIC_RG_LDO_VIBR_EN, 0);
	usleep_range(2000, 2000 + 10);

	/*OLED_DVDD_1P2_disable*/
	gpiod_set_value(ctx->dvdd_gpio, 0);

	usleep_range(2000, 2000 + 10);

	pmic_set_register_value(PMIC_RG_VM18_VOSEL, 0x0C);
	pmic_set_register_value(PMIC_RG_LDO_VM18_EN, 0);

	ctx->error = 0;
	ctx->prepared = false;
	panel->panel_initialized = false;
	ctx->doze_brightness_state = DOZE_TO_NORMAL;
	pr_info("%s-\n", __func__);
	return 0;
}

static int lcm_prepare(struct drm_panel *panel)
{
	struct lcm *ctx = panel_to_lcm(panel);
	int ret;

	pr_info("%s +\n", __func__);
	if (ctx->prepared)
		return 0;
	/*OLED_VDDI_VM18_EN*/
	pmic_set_register_value(PMIC_RG_VM18_VOSEL, 0x0C);
	pmic_set_register_value(PMIC_RG_LDO_VM18_EN, 1);
	usleep_range(1 * 1000, 1 * 1000 + 10);

	/*OLED_DVDD_1P2_EN*/
	gpiod_set_value(ctx->dvdd_gpio, 1);
	usleep_range(1 * 1000, 1 * 1000 + 10);

	/*Power on VCI_3P0*/
	pmic_set_register_value(PMIC_RG_VIBR_VOSEL, 0xB);
	pmic_set_register_value(PMIC_RG_LDO_VIBR_EN, 1);

	ret = ctx->error;
	if (ret < 0)
		lcm_unprepare(panel);

	ctx->prepared = true;
	panel->panel_initialized = true;
#ifdef PANEL_SUPPORT_READBACK
	lcm_panel_get_data(ctx);
#endif

	pr_info("%s-\n", __func__);
	return ret;
}

static int lcm_enable(struct drm_panel *panel)
{
	struct lcm *ctx = panel_to_lcm(panel);

	if (ctx->enabled)
		return 0;

	lcm_panel_init(ctx);

	if (ctx->backlight) {
		ctx->backlight->props.power = FB_BLANK_UNBLANK;
		backlight_update_status(ctx->backlight);
	}

	ctx->enabled = true;

	return 0;
}

