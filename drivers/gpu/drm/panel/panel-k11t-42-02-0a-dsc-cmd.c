static void panel_set_round_enable(struct drm_panel *panel, bool enable)
{
	struct lcm *ctx;
	char page7_enable_tb[] = {0xF0, 0x55, 0xAA, 0x52, 0x08, 0x07};
	char crc_round_conner_close[] = {0xC0, 0x00, 0x02};
	char crc_round_conner_open[] = {0xC0, 0x01, 0x02};
	int ret = 0;

	struct mtk_ddic_dsi_msg *cmd_msg =
		vmalloc(sizeof(struct mtk_ddic_dsi_msg));

	if (!panel) {
		pr_err("invalid params\n");
		vfree(cmd_msg);
		return;
	}
	ctx = panel_to_lcm(panel);
	ctx->round_enable = enable;

	pr_info("%s begin +\n", __func__);
	if (unlikely(!cmd_msg)) {
		pr_err("%s vmalloc mtk_ddic_dsi_msg is failed, return\n", __func__);
		vfree(cmd_msg);
		return;
	}
	memset(cmd_msg, 0, sizeof(struct mtk_ddic_dsi_msg));

	cmd_msg->channel = 0;
	cmd_msg->flags = 0;
	cmd_msg->tx_cmd_num = 2;

	cmd_msg->type[0] = 0x39;
	cmd_msg->tx_buf[0] = page7_enable_tb;
	cmd_msg->tx_len[0] = 6;

	cmd_msg->type[1] = 0x39;
	if(enable)
		cmd_msg->tx_buf[1] = crc_round_conner_open;
	else
		cmd_msg->tx_buf[1] = crc_round_conner_close;
	cmd_msg->tx_len[1] = 3;

	ret = mtk_ddic_dsi_wait_te_send_cmd(cmd_msg, false);
	if (ret != 0) {
		DDPPR_ERR("%s error\n", __func__);
		goto  done;
	}

done:
	vfree(cmd_msg);
	pr_info("%s %s\n", __func__, enable ? "enable" : "disable");

	return;
}

static void panel_set_bist_color(struct drm_panel *panel, u8 *rgb)
{
	struct lcm *ctx;
	char bist_page[] = {0xF0, 0x55, 0xAA, 0x52, 0x08, 0x00};
	char offset_3d_lut[] = {0x6F, 0x19};
	char disable_3d_lut[] = {0xD4, 0x00};
	char bist_color[] = {0xEF, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0x17, 0xFF};
	char bist_enable[] = {0xEE, 0x87, 0x78, 0x02, 0x40};
	struct mtk_ddic_dsi_msg *cmd_msg = NULL;
	int ret = 0;

	if (!panel) {
		pr_err("invalid params\n");
		return;
	}

	ctx = panel_to_lcm(panel);

	if (!ctx->bist_mode_enabled)
			pr_info("%s skip bist color set as bist mode is disabled\n", __func__);

	cmd_msg = vmalloc(sizeof(struct mtk_ddic_dsi_msg));

	if (unlikely(!cmd_msg)) {
		pr_err("%s vmalloc mtk_ddic_dsi_msg is failed, return\n", __func__);
		return;
	}

	memset(cmd_msg, 0, sizeof(struct mtk_ddic_dsi_msg));

	bist_color[3] = rgb[0]; /* red */
	bist_color[4] = rgb[1]; /* green */
	bist_color[5] = rgb[2]; /* blue*/

	cmd_msg->channel = 0;
	cmd_msg->flags = 2;
	cmd_msg->tx_cmd_num = 5;

	cmd_msg->type[0] = 0x39;
	cmd_msg->tx_buf[0] = bist_page;
	cmd_msg->tx_len[0] = sizeof(bist_page)/sizeof(char);

	cmd_msg->type[1] = 0x15;
	cmd_msg->tx_buf[1] = offset_3d_lut;
	cmd_msg->tx_len[1] = sizeof(offset_3d_lut)/sizeof(char);

	cmd_msg->type[2] = 0x15;
	cmd_msg->tx_buf[2] = disable_3d_lut;
	cmd_msg->tx_len[2] = sizeof(disable_3d_lut)/sizeof(char);

	cmd_msg->type[3] = 0x39;
	cmd_msg->tx_buf[3] = bist_color;
	cmd_msg->tx_len[3] = sizeof(bist_color)/sizeof(char);

	cmd_msg->type[4] = 0x39;
	cmd_msg->tx_buf[4] = bist_enable;
	cmd_msg->tx_len[4] = sizeof(bist_enable)/sizeof(char);

	ret = mtk_ddic_dsi_wait_te_send_cmd(cmd_msg, false);
	if (ret != 0) {
		DDPPR_ERR("%s mtk_ddic_dsi_send_cmd error\n", __func__);
	}

	vfree(cmd_msg);
	pr_info("%s R:%d G:%d B:%d\n", __func__, rgb[0], rgb[1], rgb[2]);

	return;
}

